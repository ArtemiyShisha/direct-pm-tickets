import { getProductCatalogForPrompt } from "@/knowledge/direct-context";
import { CRITERIA } from "@/lib/types";

export function buildPreAnalysisPrompt(): string {
  const productCatalog = getProductCatalogForPrompt();

  const criteriaList = CRITERIA.map((c) => `  - ${c.id}: ${c.label}`).join("\n");

  return `Ты — аналитик, который готовит контекст для ревью эпика Яндекс Директа.

Твоя задача — прочитать текст эпика и определить:

1. **epic_type** — тип эпика:
   - compliance: вызван внешним требованием (санкции, регуляторика, законодательство)
   - migration: миграция legacy-систем, перевод на новую платформу
   - product_feature: новая продуктовая фича или изменение существующей
   - tech_debt: техдолг, рефакторинг, оптимизация без продуктовых изменений
   - infrastructure: инфраструктурные изменения (мониторинг, деплой, CI/CD)
   - experiment: A/B-тест, эксперимент, валидация гипотезы

2. **product_ids** — к каким продуктам Директа относится эпик. Определи по тексту, используя каталог продуктов ниже. Если эпик затрагивает платформу целиком (БК, Модерация, Баланс и т.д.) — включи "platform". Можно указать несколько.

3. **na_criteria** — какие критерии оценки НЕПРИМЕНИМЫ к данному эпику.

   ВАЖНЫЕ ПРАВИЛА для N/A:
   - potential: N/A если эпик вызван внешним требованием и оценка бизнес-потенциала неуместна
   - analytics: N/A если решение продиктовано внешним требованием, а не гипотезами на данных
   - design: N/A если эпик НЕ содержит изменений пользовательского интерфейса
   - onboarding: N/A если фича не меняет пользовательский опыт или не требует обучения
   - international: N/A если продукт работает на одном рынке и не зависит от региона/языка
   - interfaces: если у продукта нет некоторых каналов (API, Коммандер, Excel), НЕ ставь N/A — но укажи в product_context_note какие каналы отсутствуют
   
   НЕ ставь N/A если критерий применим, но плохо описан в эпике — это задача оценки, а не пред-анализа.

4. **solution_summary** — найди в ВЕРХНЕМ разделе эпика (до начала детальных требований) краткое описание того, что делается. Обычно это ответ на вопрос "Что делаем?" из чеклиста. Процитируй дословно. Если такого раздела нет — null.

5. **product_context_note** — напиши краткий абзац (2-4 предложения) для оценочной модели: какой продукт затронут, какие интерфейсы есть/нет, что важно учесть. Пример: "Эпик относится к Рекламе в Телеграме, которая настраивается через ЕПК в Директ Про. У продукта нет отдельного API, Коммандера или Excel-экспорта. Рынки: RU, BY, UZ, KZ, TR."

КАТАЛОГ ПРОДУКТОВ ЯНДЕКС ДИРЕКТА:
${productCatalog}

СПИСОК КРИТЕРИЕВ ОЦЕНКИ:
${criteriaList}`;
}
