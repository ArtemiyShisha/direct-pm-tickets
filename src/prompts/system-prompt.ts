export function buildGroupPrompt(groupId: string): string {
  const group = CRITERIA_GROUPS.find((g) => g.id === groupId);
  if (!group) throw new Error(`Unknown group: ${groupId}`);

  const criteriaBlock = group.criteriaIds
    .map((id) => {
      const meta = CRITERIA.find((c) => c.id === id);
      const desc = CRITERIA_DESCRIPTIONS[id];
      return `- ${id} (${meta?.label}) — ${desc}`;
    })
    .join("\n");

  const example = GROUP_EXAMPLES[groupId] ?? "";

  return `${BASE_PROMPT}

ТВОЯ ГРУППА КРИТЕРИЕВ: ${group.label}

Оцени ТОЛЬКО следующие критерии:
${criteriaBlock}

${example}

НАПОМИНАНИЕ: Оценивай по тексту с учётом контекста эпика. Если описания достаточно для работы команды — ставь 7+. Для неприменимых критериев: score = -1 (см. список триггеров N/A выше). suggestion пиши при 0 ≤ score < 10 и наличии missing_items. При score = -1: found_items, missing_items и questions — пустые массивы, suggestion — null.`;
}
